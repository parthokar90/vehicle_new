function showLoginActivity() {
    load_spinner_activity('show', 'login_activity_loader', '');
    $.ajax({
        type: "GET",
        dataType: "html",
        url: "<?php echo e(url('dashboard/getLoginActivity')); ?>/" + "<?php echo e(auth()->user()->id); ?>",
        success: function(response) {
            $('#load-login-activity').html(response);
            load_spinner_activity('hide', 'login_activity_loader', '');
        },
    });
}

function load_spinner_activity(type, hide_element, show_element) {
    if (type == 'hide') {
        $("#" + hide_element).removeClass('show');
        $("#" + hide_element).addClass('d-none');
        //$("#" + show_element).removeClass('d-none');
        //$("#" + show_element).addClass('show');
    }
    if (type == 'show') {
        // $("#" + show_element).removeClass('show');
        // $("#" + show_element).addClass('d-none');
        $("#" + hide_element).removeClass('d-none');
        $("#" + hide_element).addClass('show');
    }

}
